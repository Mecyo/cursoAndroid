    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!radioPF.isSelected() && !radioPJ.isSelected()) {
            alertMessage(this, "Selecione um tipo de pessoa e preencha os campos para cadastrar!", "Atenção!");
            return;
        } else if (radioPF.isSelected()) {
            pessoa = new PessoaFisica(txtNome.getText(), txtTelefone.getText(), txtEndereco.getText(), txtRGIE.getText(), txtCpfCnpj.getText());
        } else {
            pessoa = new PessoaJuridica(txtNome.getText(), txtTelefone.getText(), txtEndereco.getText(), txtRGIE.getText(), txtCpfCnpj.getText());
        }
        
        if(conttroller.cadastrar(pessoa, posicao)){
            successMessage(this, "Cadastro efetuado com sucesso!", "OK!");
            clear();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

