    private void verificarCamposPessoa(Boolean habilita) {
        if(habilita){
            lblRGIE.setText("RG:");
            lblCpfCnpj.setText("CPF:");
        }else{
            lblRGIE.setText("IE:");
            lblCpfCnpj.setText("CNPJ:");
        }
        frameDadosEspecificos.setVisible(true);
    }
    
    public CadastroPessoa getConttroller() {
        return conttroller;
    }
    
    public void clear(){
        txtNome.setText("");
        txtTelefone.setText("");
        txtEndereco.setText("");
        txtRGIE.setText("");
        txtCpfCnpj.setText("");
        groupRadio.clearSelection();
        radioPJ.setSelected(false);
        lblTipoPessoa.setText("");
        frameDadosEspecificos.setVisible(false);
    }
    
    public void alterar(Pessoa pessoa){
        txtNome.setText(pessoa.getNome());
        txtTelefone.setText(pessoa.getTelefone());
        txtEndereco.setText(pessoa.getEndereco());
        if (pessoa instanceof PessoaFisica) {
            txtRGIE.setText(((PessoaFisica) pessoa).getRG());
            txtCpfCnpj.setText(((PessoaFisica) pessoa).getCPF());
            lblTipoPessoa.setText(radioPF.getText().toUpperCase());  
            radioPF.setSelected(true);
            radioPJ.setSelected(false);
        }else{
            txtRGIE.setText(((PessoaJuridica) pessoa).getIE());
            txtCpfCnpj.setText(((PessoaJuridica) pessoa).getCNPJ());
            lblTipoPessoa.setText(radioPJ.getText().toUpperCase());  
            radioPF.setSelected(false);
            radioPJ.setSelected(true);
        }
        this.posicao = conttroller.getIndex(pessoa);
    }
    
    
